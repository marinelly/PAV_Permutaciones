    public Ventana() {
        initComponents();
        lienzo = new Lienzo();
        lienzo.setBackground(Color.LIGHT_GRAY);
        lienzo.setSize(jPanel1.getSize());
        lienzo.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getButton() == 1) { //Boton Izquierdo
                    Nodo nodo = new Nodo(e.getX(), e.getY(), lienzo.nodos.size()+"");
                    
                    for(int i=0; i< lienzo.nodos.size(); i++){
                        Nodo vecino = lienzo.nodos.get(i);
                        double distancia = Math.sqrt(Math.pow(nodo.x - vecino.x, 2) + Math.pow(nodo.y - vecino.y, 2));
                        double valor = (double)Math.round(distancia * 100 ) / 100;
                        
                        lienzo.distancias[i][lienzo.nodos.size()] = valor;
                        lienzo.distancias[lienzo.nodos.size()][i] = valor;

                    }
                    
                    lienzo.nodos.add(nodo);
                    lienzo.repaint();
                    
                }
            }
        });

        jPanel1.add(lienzo);


    }

